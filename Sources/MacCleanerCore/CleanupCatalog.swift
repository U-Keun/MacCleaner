import Foundation

public enum RemoveStrategy: Sendable {
    case contents
    case item
    case simulatorData
    case scanOnlyContents
    case desktopDevCaches
}

public enum CleanupAvailability: Sendable {
    case cleanable
    case itemized
    case scanOnly
}

public struct CleanupPath: Hashable, Sendable {
    public let rawValue: String
    public let removeStrategy: RemoveStrategy

    public init(rawValue: String, removeStrategy: RemoveStrategy) {
        self.rawValue = rawValue
        self.removeStrategy = removeStrategy
    }

    public var expandedPath: String {
        (rawValue as NSString).expandingTildeInPath
    }

    public var displayPath: String {
        rawValue
    }
}

public struct CleanupCategory: Hashable, Sendable {
    public let id: String
    public let name: String
    public let description: String
    public let paths: [CleanupPath]
    public let requiresFullDiskAccess: Bool
    public let cleanupAvailability: CleanupAvailability

    public init(
        id: String,
        name: String,
        description: String,
        paths: [CleanupPath],
        requiresFullDiskAccess: Bool = false,
        cleanupAvailability: CleanupAvailability = .cleanable
    ) {
        self.id = id
        self.name = name
        self.description = description
        self.paths = paths
        self.requiresFullDiskAccess = requiresFullDiskAccess
        self.cleanupAvailability = cleanupAvailability
    }
}

public enum CleanupCatalogError: Error, LocalizedError, Sendable {
    case unknownCategory(String)

    public var errorDescription: String? {
        switch self {
        case .unknownCategory(let token):
            return "Unknown category: \(token)"
        }
    }
}

