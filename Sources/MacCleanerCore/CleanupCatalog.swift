    public static func resolve(_ tokens: [String]) throws -> [CleanupCategory] {
        if tokens.isEmpty || tokens.map({ $0.lowercased() }).contains("all") {
            return categories
        }

        var selected: [CleanupCategory] = []
        var seen: Set<String> = []

        for token in tokens {
            let key = token.lowercased()
            guard let categoryID = aliases[key],
                  let category = categoriesByID[categoryID] else {
                throw CleanupCatalogError.unknownCategory(token)
            }

            if seen.insert(categoryID).inserted {
                selected.append(category)
            }
        }

        return selected
    }
